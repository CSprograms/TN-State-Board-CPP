#include <iostream>
using namespace std;
int main()
{
    double varf = 178.25255685;

    cout<<varf<<endl;

    cout<<(float)varf<<endl;

    cout<<(int)varf<<endl;

    cout<<"Double : "<<sizeof(double)<<endl;
    cout<<"Float : "<<sizeof(float)<<endl;

return 0;
}

